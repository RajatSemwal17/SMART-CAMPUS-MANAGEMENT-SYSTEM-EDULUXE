const groupSelect = document.querySelector('.selectGroup');
const generate = document.querySelector('.generate');
const table = document.querySelector('table');

const timeSlots = ["9:00 - 10:00", "10:00 - 11:00", "11:00 - 12:00", "12:00 - 1:00", "1:00 - 2:00", "2:00 - 3:00", "3:00 - 4:00"];

const days = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday"];

generate.addEventListener('click', () => {
  const selectedGroup = groupSelect.value;
  if(!selectedGroup || !timetableData[selectedGroup]){
    alert("Please Select An Option.");
    return;
  }

  // Start building the table HTML;
  let html = `<thead>
  <tr><th>TIME</th>
  `;

  timeSlots.forEach((time) => {
    html += `
      <th>
        ${time}
      </th>
    `
  })
  html += "</tr></thead><tbody>";

  // Loop through Each Day:
  days.forEach((day) => {
    html += `<tr><td><strong>${day}</strong></td>`;

    timeSlots.forEach((time) => {
    const val = timetableData[selectedGroup][day][time] || "";
    if (val === "BREAK") {
      html += `<td style="background-color: rgba(64, 255, 0, 0.52);">${val}</td>`;
    } else {
      html += `<td>${val}</td>`;
    }
    })
    html += '</tr>';
  });
    html += '</tbody>';
    table.innerHTML = html;
});


